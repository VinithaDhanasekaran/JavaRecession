public class Total_And_Average{
    public static void main(String args[]){
        int Tam,Eng,Mat,total;
        float average;
        
        Tam=99;
        Eng=95;
        Mat=100;
        
        total=Tam+ Eng +Mat;
        System.out.println("Total : "+ total);
        average= total/3;
        System.out.print("Average : "+ average);
        
    }
}

